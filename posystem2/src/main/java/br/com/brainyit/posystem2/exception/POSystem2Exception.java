public class POSystem2Exception extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2063412093327148579L;

	public POSystem2Exception() {
		super();
		
	}

	public POSystem2Exception(String message, Throwable cause) {
		super(message, cause);
		
	}

	public POSystem2Exception(String message) {
		super(message);
		
	}

	public POSystem2Exception(Throwable cause) {
		super(cause);
		
	}

	
	
}
